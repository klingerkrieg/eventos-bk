      <p align="justify">A <strong>Expotec</strong> é um evento realizado pelos campi do Instituto 
      Federal de Educação, Ciência e Tecnologia do Rio Grande do Norte (IFRN) para divulgar projetos de 
      ensino, pesquisa e extensão, submeter trabalhos científicos, ofertar palestras, oficinas e minicursos, 
      bem como promover atividades artístico-culturais. </p>

      <p align="justify">Em 2021, o Campus Nova Cruz realiza a sua <b>VII Expotec</b> em formato totalmente virtual,
       devido à pandemia causada pelo novo coronavírus. O evento apresenta uma mostra de trabalhos produzidos 
       por estudantes e servidores do campus, proporcionando, assim, desenvolvimento humano, social, científico
        e tecnológico a toda a comunidade.</p>

      <p align="justify">A <b>VII Expotec</b> celebra o tema “Todas as Ciências são Essenciais à Sociedade”. 
      A escolha desse tema é um contraponto ao atual cenário de negação do pensamento científico, 
      em que até mesmo os princípios mais básicos das ciências vêm sendo postos em dúvida por crenças 
      e achismos – pensou-se – há muito superados. </p>

      <p align="justify">Nesse contexto, o Campus Nova Cruz do IFRN advoga que as ciências, todas elas, 
      sobretudo em tempos de pandemia, são as melhores ferramentas para produzir as soluções capazes de 
      (re)conduzir a sociedade ao caminho do desenvolvimento, constituindo-se, assim, em instrumentos 
      essenciais para a superação do obscurantismo vivido atualmente.	</p>
      
      
